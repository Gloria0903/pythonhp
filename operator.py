#arithmetic operators
a=4
b=5
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)  #remainder

#comparison operator
c=56
b=21

print(f"{c} is equals to {b} {c==b}")
print(f"{c} is not equal to {b} {c!=b}")
print(f"{c} is greater than {b} {c>b}")
print(f"{c} is greater than or equals to {b} {c>=b}")
print(f"{c} is less than  {b} {c<b}")
print(f"{c} is less than or equals to  {b} {c<=b}")

# logical operator
a=7
print(a>4 and a>7)
print(a>4 or a>7)

